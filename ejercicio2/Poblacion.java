package ejercicio2;

public class Poblacion {
	double pobla;
	String dpdo;
	public Poblacion(double pobla, String dpdo) {
		super();
		this.pobla = pobla;
		this.dpdo = dpdo;
	}
	public double getPobla() {
		return pobla;
	}
	public void setPobla(double pobla) {
		this.pobla = pobla;
	}
	public String getDpdo() {
		return dpdo;
	}
	public void setDpdo(String dpdo) {
		this.dpdo = dpdo;
	}


}
