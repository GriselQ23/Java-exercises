package ejercicio1;

public class VentaMes {
	 double venta;
	 String mes;
	public VentaMes(double venta, String mes) {
		super();
		this.venta = venta;
		this.mes = mes;
	}
	public double getVenta() {
		return venta;
	}
	public void setVenta(double venta) {
		this.venta = venta;
	}
	public String getMes() {
		return mes;
	}
	public void setMes(String mes) {
		this.mes = mes;
	}
	 
	 
	}

